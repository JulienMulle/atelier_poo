public class Monster {
    private String name;
    private int damage;
    private int life;

    public String getName() {
        return this.name;
    }
    public void setName(String name) {
        this.name = name;
    }

    public int getDamage() {
        return this.damage;
    }
    public void setDamage(int damage) {
        this.damage = damage;
    }

    public int getLife() {
        return this.life;
    }
    public void setLife(int life) {
        this.life= life;
    }

}